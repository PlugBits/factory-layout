export function getTemplateName(language: Language, templateId: string, fallback: string) {
  return templateNames[language][templateId] ?? templateNames.ja[templateId] ?? fallback;
}

export function getItemDisplayName(language: Language, templateId: string, currentName: string) {
  const knownNames = languages
    .map(({ code }) => templateNames[code][templateId])
    .filter(Boolean);
  return knownNames.includes(currentName) ? getTemplateName(language, templateId, currentName) : currentName;
}
